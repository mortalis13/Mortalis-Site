<template>
<div>
<p>The plugins are created with the <a href="https://www.sublimetext.com/docs/3/api_reference.html" target="_blank"><b>Sublime API</b></a> and are mainly used for text editing and interface control as well as filesystem manipulation.</p>
<p>Description of some scripts:</p>
<ul>
  <li><b>convert_sql</b> - transforms SQL <b>INSERT</b> statements to <b>UPDATE</b> inside the selected text</li>
  <li><b>create_files</b> - creates multiple files with the specified content</li>
  <li><b>find_duplicated_lines</b> - shows duplicated lines in a new tab</li>
  <li><b>generate_from_template</b> - creates text using a static template and dynamic variables</li>
  <li><b>generate_java_getters</b> - creates getter/setter methods for class fields in <b>Java</b></li>
  <li><b>generate_lines</b> - creates lines following a pattern</li>
  <li><b>get_method_call_signature</b> - generates method call from its definition</li>
  <li><b>open_all_files</b> - opens files in tabs under a selected folder in the sidebar</li>
  <li><b>open_project_in_new_window</b> - clones active project window with the same project folder</li>
  <li><b>paste_with_commas</b> - pastes a list of lines separated by commas (with/without quotes)</li>
  <li><b>separate_variable</b> - creates separate lines for a variable definition and its usage in a statement</li>
  <li><b>subtract_lines</b> - finds a difference between 2 lists of lines</li>
  <li><b>unindent_resursive</b> - reduces a nested block indentation by the same amount for all nested levels</li>
  <li><b>view_as_hex</b> - converts all the text to hexadecimal codepoints</li>
</ul>
</div>
